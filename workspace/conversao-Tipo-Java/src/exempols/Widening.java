package exempols;

public class Widening {
	public static void main(String[] args) {
		byte num3 = 14;
		int num4 = 120;
		
		double resultadoDiv2 = num3 / num4;
		System.out.println(resultadoDiv2);
		
		float num5 = 14;
		int num6 = 120;
		
		double resultadoDiv3 = num5 / num6;
		System.out.println(resultadoDiv3);
	}

}
